/// Отдел
public class Department {

    /// Уникальный идентификатор
    private int id;

    /// Название отдела
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
